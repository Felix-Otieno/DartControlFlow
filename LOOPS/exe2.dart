void main() {
  var score = 80;

  switch (score) {
    case 100:
      print("You got an A");
      break;
    case 90:
      print("You got B");
      break;
    case 80:
      print("You got C");
      break;
    case 70:
      print("You got D");
      break;
    case 60:
      print("You got E");
      break;
    default:
      print("You got F");
      break;
  }
}
